def migratoryBirds(arr):
    # Write your code here
    birds = [0]*5
    for bird in arr:
        birds[bird-1] += 1

    collections = []

    for i in range(len(birds)):
        if birds[i] == max(birds):
            collections.append(i+1)

    return collections[0]
    
    

